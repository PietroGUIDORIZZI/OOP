package model;

public class Pet {
    private String name;
    private int petAge;


    public Pet() {

    }

    public Pet(String name) {
        this.name = name;
        this.petAge = petAge;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPetAge() {
        return petAge;
    }

    public void setPetAge(int petAge) {
        this.petAge = petAge;
    }

}
